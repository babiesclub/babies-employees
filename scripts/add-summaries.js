(async () => {
  const snap = await db.collection('materials').get();
  const mats = [];
  snap.forEach(d => mats.push({ id: d.id, ...d.data() }));
  mats.sort((a, b) => (a.name || '').localeCompare(b.name || '', 'he'));

  let toWrite = 0, missing = 0, hasAlready = 0;
  const missingNames = [];

  console.log(`\n📦 בודק ${mats.length} מערכים...\n`);

  for (const m of mats) {
    const summary = SUMMARIES[m.name];
    const hasExisting = m.summary && m.summary.trim();

    if (!summary) {
      missingNames.push(m.name);
      missing++;
      console.log(`  ⚠ ${m.name.padEnd(30)} → אין תמצית מוכנה בסקריפט`);
      continue;
    }

    if (hasExisting && !OVERWRITE) {
      hasAlready++;
      continue;
    }

    toWrite++;
    console.log(`  ${APPLY ? '✓' : '·'} ${m.name.padEnd(30)} → ${summary.slice(0, 60)}...`);
  }

  console.log(`\n📊 סיכום:`);
  console.log(`   • ${toWrite} מערכים ${APPLY ? 'נכתבים עכשיו' : 'מוכנים לכתיבה'}`);
  console.log(`   • ${hasAlready} כבר עם תמצית (דלגנו - העבר --overwrite אם רוצים לדרוס)`);
  console.log(`   • ${missing} מערכים בלי תמצית מוכנה (צריך להוסיף לסקריפט)`);

  if (missingNames.length) {
    console.log(`\n⚠ חסרים:`);
    missingNames.forEach(n => console.log(`     - ${n}`));
  }

  if (!APPLY) {
    console.log(`\n💡 זה היה מצב תצוגה. להפעיל בפועל: node add-summaries.js --apply`);
    process.exit(0);
  }

  console.log(`\n🚀 כותב ל-Firestore...`);
  let written = 0, failed = 0;
  for (const m of mats) {
    const summary = SUMMARIES[m.name];
    const hasExisting = m.summary && m.summary.trim();
    if (!summary || (hasExisting && !OVERWRITE)) continue;
    try {
      await db.collection('materials').doc(m.id).update({ summary });
      written++;
      process.stdout.write('.');
    } catch (e) {
      failed++;
      console.error(`\n  ✗ ${m.name}: ${e.message}`);
    }
  }

  console.log(`\n\n✅ סיים! ${written} נכתבו, ${failed} נכשלו.`);
  process.exit(0);
})().catch(e => {
  console.error('Error:', e);
  process.exit(1);
});
